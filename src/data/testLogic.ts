import { TestAnswer, TestResult } from '@/types/test';

export function calculateTestResult(answers: TestAnswer[]): TestResult {
    const answerMap = new Map(answers.map(a => [a.questionId, a.answer]));

    const q1 = answerMap.get(1) || '';
    const q2 = answerMap.get(2) || '';
    const q3 = answerMap.get(3) || '';
    const q4 = answerMap.get(4) || '';
    const q5 = answerMap.get(5) || '';
    const q6 = answerMap.get(6) || '';

    // ВАРИАНТ 1: сухая кожа + сухость + кремы + работало/не разбираюсь + 5-10 мин/как получится + cruelty free/vegan/важны
    if (
        q1 === 'сухой' &&
        (q2 === 'сухость' || q2 === 'ничего не беспокоит') &&
        (q3 === 'кремы' || q3 === 'пока не знаю') &&
        (q4 === 'главное, чтобы работало' || q4 === 'пока не разбираюсь') &&
        (q5 === '5-10 минут' || q5 === 'как получится') &&
        (q6 === 'cruelty free' || q6 === 'vegan' || q6 === 'важны, но пока разбираюсь')
    ) {
        return {
            variant: 1,
            productIds: ['9', '2', '3', '13', '9', '8'] // ID карточек из раздела "лицо"
        };
    }

    // ВАРИАНТ 2: комбинированная/нормальная/жирная + высыпания/ничего/чувствительность + масла/не знаю + работало/не разбираюсь/натуральное
    if (
        (q1 === 'комбинированный' || q1 === 'нормальный' || q1 === 'жирный') &&
        (q2 === 'высыпания' || q2 === 'ничего не беспокоит' || q2 === 'чувствительность') &&
        (q3 === 'масла, бальзамы' || q3 === 'пока не знаю') &&
        (q4 === 'главное, чтобы работало' || q4 === 'пока не разбираюсь' || q4 === 'максимально натуральное')
    ) {
        return {
            variant: 2,
            productIds: ['16', '19', '8', '2', '9', '8']
        };
    }

    // ВАРИАНТ 3: любые ответы + легкие гели/не знаю
    if (q3 === 'легкие гели' || q3 === 'пока не знаю') {
        return {
            variant: 3,
            productIds: ['18', '16', '13', '11', '9', '8']
        };
    }

    // По умолчанию возвращаем вариант 3
    return {
        variant: 3,
        productIds: ['18', '16', '13', '11', '9', '8']
    };
}
